	) ABSNT GROUP BY ABSNT.BRANCH_ID,ABSNT.EMPCODE
	) T ON EMPATT.BRANCH_ID=T.BRANCH_ID 
	AND EMPATT.EMPCODE=T.EMPCODE
	--End of Rev 2.0

