			;with cte as(SELECT	
			EMPCODE,RPTTOEMPCODE
			FROM #EMPHRS 
			WHERE EMPCODE IS NULL OR EMPCODE=@empcodes  
			UNION ALL
			SELECT	
			a.EMPCODE,a.RPTTOEMPCODE
			FROM #EMPHRS a
			join cte b
			ON a.RPTTOEMPCODE = b.EMPCODE
			) 
			INSERT INTO #EMPHR_EDIT
			SELECT EMPCODE,RPTTOEMPCODE FROM cte 

