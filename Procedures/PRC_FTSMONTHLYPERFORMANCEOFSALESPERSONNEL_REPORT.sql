	SET @StrSql+=''+@BRANDPRODNAME_OS+',[VALUE_OS],[SECONDARY SALES],'+@BRANDPRODNAME_SS+',[VALUE_SS],[CLOSING STOCKS],'+@BRANDPRODNAME_CS+',[PRIMARY],'+@BRANDPRODNAME_PS+',[VALUE_PS] FROM #TEMPPERFORM_FINAL '
