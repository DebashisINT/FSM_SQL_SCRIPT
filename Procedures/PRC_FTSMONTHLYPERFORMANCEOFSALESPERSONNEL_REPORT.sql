				BEGIN
					--OPENING STOCK UPDATE
					IF (EXISTS (SELECT PRODNAME_OPENING FROM #TMPOPENINGSTOCK WHERE PRODNAME_OPENING=LTRIM(RTRIM(@PRODNAME))+'_OS' AND EMPCODE=@M_EMPCODE AND SHOP_CODE=@M_SHOP_CODE))
